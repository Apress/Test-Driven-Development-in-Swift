import XCTest

class AlbertosUITests: XCTestCase {

    func testExample() throws {
        // Making the tests fail intentionally while there's no real test so we don't accidentally
        // add them to the scheme that runs the unit tests only without noticing.
        //
        // Practicing Test-Driven Development is all about establishing as fast a feedback cycle as
        // possible. Running tests that are slow to launch and on top of that do nothing is an
        // unnecessary drag.
        XCTFail("No test implemented yet!")
    }
}
